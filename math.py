import math

pi = 3.14
x = 5
y = 10
z = 20

print(round(pi)) #round a number to the nearest
print(math.ceil(pi)) #round a number to up
print(math.floor(pi)) #round a number to down
print(abs(pi)) #return the absolute number (if is negative, return a positive number)
print(pow(pi,2)) #return a power
print(math.sqrt(420)) #return a square of a number
print(max(x,y,z)) #return the highest number
print(min(x,y,z)) #return the smalest number
